import { BrowserRouter as Router, Switch, Route } from "react-router-dom";

// Routing
import PrivateRoute from "./components/routing/PrivateRoute";

// Screens
import PrivateScreen from "./components/screens/authentication/PrivateScreen";
import LoginScreen from "./components/screens/authentication/LoginScreen";
import RegisterScreen from "./components/screens/authentication/RegisterScreen";
import ForgotPasswordScreen from "./components/screens/authentication/ForgotPasswordScreen";
import ResetPasswordScreen from "./components/screens/authentication/ResetPasswordScreen";

const App = () => {
  return (
    <Router>
      <div className="app">
        <Switch>
          <PrivateRoute exact path="/" component={PrivateScreen} />
          <Route exact path="/login" component={LoginScreen} />
          <Route exact path="/register" component={RegisterScreen} />
          <Route
            exact
            path="/forgotpassword"
            component={ForgotPasswordScreen}
          />
          <Route
            exact
            path="/passwordreset/:resetToken"
            component={ResetPasswordScreen}
          />
        </Switch>
      </div>
    </Router>
  );
};

export default App;
